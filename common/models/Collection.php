    public function init()
    {
        $jsonAttributes = [];
        $rules = [];
        $slugAttributes = [];
        foreach (self::$schemas as $schema) {
            switch ($schema["type"]) {
                case SystemCmsCollectionAlias::TYPE_JSON:
                    $jsonAttributes[] = $schema["name"];
                    break;
                case SystemCmsCollectionAlias::TYPE_SLUG:
                    if (isset($schema["options"]["ref_column"])) {
                        $slugAttributes[$schema["name"]] = $schema["options"]["ref_column"];
                    }
                    break;
            }
            $rules[] = $this->getRuleBySchema($schema);
        }
        $this->rules = $rules;
        $this->jsonAttribute = $jsonAttributes;
        $this->slugAttribute = $slugAttributes;
        parent::init(); // TODO: Change the autogenerated stub
    }

