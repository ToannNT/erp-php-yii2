    public function getRelationCms($schema)
    {
        $options = $schema["options"];
        $collection = SystemCmsCollection::find()->where(["name" => $options["ref_table"]])->one();
        if (!$collection) {
            return [];
        }
        Collection::setFields($collection->schemas);
        Collection::setTableName($collection->name);
        if (empty($this->{$schema["name"]})) {
            return [];
        }
        $query = clone $this::find();
        $query = $query->where(["id" => explode(",", $this->{$schema["name"]})]);
        if ($options["type"] === SystemCmsCollection::TYPE_RELATION_MULTIPLE) {
            return $query->all();
        }
        return $query->one();
    }

    public function getRelationCore($schema)
    {
        /**
         * @var Query $query
         */
        $options = $schema["options"];
        $rowValues = explode(",", $this->{$schema["name"]});
        $result = new GetRelationCore([
            "tableName" => $options["ref_table"],
            "rowValues" => $rowValues,
            "type" => $options["type"]
        ]);
        return $result->result();
    }

    /**
     * @param $schema
     * @return array
     */
    public function getRuleBySchema($schema): array
    {
        $rule = [];
        switch ($schema["type"]) {
            case SystemCmsCollectionAlias::TYPE_TEXT:
            case SystemCmsCollectionAlias::TYPE_SLUG:
                $rule = [$schema["name"], StringValidator::class, "max" => 400];
                break;
            case SystemCmsCollectionAlias::TYPE_BOOLEAN:
                $rule = [$schema["name"], BooleanValidator::class];
                break;
            case SystemCmsCollectionAlias::TYPE_EMAIL:
                $rule = [$schema["name"], EmailValidator::class];
                break;
            case SystemCmsCollectionAlias::TYPE_NUMBER:
                $rule = [$schema["name"], NumberValidator::class];
                break;
            default:
                $rule = [$schema["name"], SafeValidator::class];
        }
        return $rule;
    }

